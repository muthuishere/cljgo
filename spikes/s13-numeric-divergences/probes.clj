;; S13 numeric-tower divergence probes.
;; Runnable unmodified by both:
;;   clojure -M probes.clj                      (oracle: real Clojure 1.12.5)
;;   go run ./cmd/cljgo run probes.clj           (from repo root; cljgo under test)
;;
;; Each probe prints "LABEL => VALUE-OR-THREW:message" on one line, via pr-str
;; so read-syntax suffixes (N / M / ##Inf / ##NaN / ratios) are preserved.
;; NOTE: a macro-based try/catch wrapper silently fails to catch panics in
;; cljgo (a separate, non-numeric divergence -- see VERDICT.md); use a plain
;; function `safe` over a thunk instead, which works reliably in both hosts.

(defn safe [f]
  (try (pr-str (f)) (catch Exception e (str "THREW:" (ex-message e)))))

(defn p [label f] (println (str label " => " (safe f))))

