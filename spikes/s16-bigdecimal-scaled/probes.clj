;; S16 BigDecimal scaled-decimal probes.
;; Runnable unmodified by both:
;;   clojure -M probes.clj                      (oracle: real Clojure 1.12.5)
;;   go run ./cmd/cljgo run probes.clj           (from repo root; cljgo baseline)
;;
;; Each probe prints "LABEL => VALUE-OR-THREW:message" on one line, via pr-str
;; so read-syntax suffixes (M / N / E-notation) are preserved. Same safe/p
;; pattern as S13 (macro try/catch wrappers are unreliable in cljgo).
;;
;; with-precision probes live in probes_wp.clj — the symbol does not resolve
;; in cljgo, which would abort every form after it in this file.

(defn safe [f]
  (try (pr-str (f)) (catch Exception e (str "THREW:" (ex-message e)))))

(defn p [label f] (println (str label " => " (safe f))))

