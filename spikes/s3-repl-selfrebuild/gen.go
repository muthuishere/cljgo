// Registry generation: a minimal genpkg (cf. refs/glojure/internal/genpkg).
// Walks each package's go/types scope and emits register() calls for every
// exported, non-generic func. Funcs only — enough for the spike.
package main

import (
	"fmt"
	"go/format"
	"go/importer"
	"go/token"
	"go/types"
	"os"
	"sort"
	"strings"
)

func generateRegistry(pkgPaths []string, outFile string) (int, error) {
	sort.Strings(pkgPaths)

	fset := token.NewFileSet()
	// "source" importer type-checks from source; in module mode go/build
	// resolves paths via the go command, so module-cache packages work.
	imp := importer.ForCompiler(fset, "source", nil)

	type pkgFuncs struct {
		path  string
		alias string
		funcs []string
	}
	var pkgs []pkgFuncs
	total := 0

	for _, path := range pkgPaths {
		p, err := imp.Import(path)
		if err != nil {
			return 0, fmt.Errorf("import %s: %w", path, err)
		}
		pf := pkgFuncs{path: path, alias: path[strings.LastIndex(path, "/")+1:]}
		scope := p.Scope()
		for _, name := range scope.Names() {
			obj := scope.Lookup(name)
			fn, ok := obj.(*types.Func)
			if !ok || !fn.Exported() {
				continue
			}
			if sig := fn.Type().(*types.Signature); sig.TypeParams().Len() > 0 {
				continue // generic funcs need instantiation; out of scope
			}
			pf.funcs = append(pf.funcs, name)
		}
		total += len(pf.funcs)
		pkgs = append(pkgs, pf)
	}

	b := &strings.Builder{}
	fmt.Fprintf(b, "// Code generated by s3repl :add-dep. DO NOT EDIT.\n\npackage main\n\n")
	fmt.Fprintf(b, "import (\n")
	for _, p := range pkgs {
		fmt.Fprintf(b, "\t%s %q\n", importAlias(p.path), p.path)
	}
	fmt.Fprintf(b, ")\n\nfunc init() {\n")
	for _, p := range pkgs {
		fmt.Fprintf(b, "\t// package %s: %d funcs\n", p.path, len(p.funcs))
		fmt.Fprintf(b, "\tregisterPkg(%q, %q)\n", p.alias, p.path)
		ia := importAlias(p.path)
		for _, name := range p.funcs {
			fmt.Fprintf(b, "\tregister(%q, %q, %s.%s)\n", p.path, name, ia, name)
		}
	}
	fmt.Fprintf(b, "}\n")

	src, err := format.Source([]byte(b.String()))
	if err != nil {
		return 0, fmt.Errorf("format: %w", err)
	}
	return total, os.WriteFile(outFile, src, 0o644)
}

func importAlias(path string) string {
	return "pkg_" + strings.NewReplacer(".", "_", "/", "_", "-", "_").Replace(path)
}
