package briaot_test

import (
	"bytes"
	"os"
	"os/exec"
	"path/filepath"
	"testing"
)

// TestGeneratedBriIsUpToDate re-runs cmd/genbri into a temp dir and
// compares byte-for-byte with the committed packages (mirroring coreaot's
// drift test, ADR 0071 / ADR 0046). The AOT-compiled bri framework is
// checked in — a `go build ./...` of a fresh clone must work and emitted
// bri binaries blank-import it — so the ONE way it can lie is by drifting
// from core/bri/*.cljg: edit a bri source, forget `go generate ./pkg/briaot`,
// and interpreted bri (pkg/briloader) and compiled bri would silently
// disagree — the CLAUDE.md REPL↔binary release blocker.
//
// Emission is deterministic (design/04 §6), so a diff here means exactly
// one thing: regenerate.
func TestGeneratedBriIsUpToDate(t *testing.T) {
	if testing.Short() {
		t.Skip("skipping bri regeneration in -short mode")
	}
	tmp := t.TempDir()
	cmd := exec.Command("go", "run", "./cmd/genbri", "-o", tmp)
	cmd.Dir = filepath.Join("..", "..")
	if out, err := cmd.CombinedOutput(); err != nil {
		t.Fatalf("genbri: %v\n%s", err, out)
	}

	var checked int
	err := filepath.Walk(tmp, func(path string, fi os.FileInfo, err error) error {
		if err != nil || fi.IsDir() {
			return err
		}
		rel, err := filepath.Rel(tmp, path)
		if err != nil {
			return err
		}
		fresh, err := os.ReadFile(path)
		if err != nil {
			return err
		}
		committed, err := os.ReadFile(rel)
		if err != nil {
			t.Errorf("%s: generated by cmd/genbri but not committed (run: go generate ./pkg/briaot)", rel)
			return nil
		}
		if !bytes.Equal(fresh, committed) {
			t.Errorf("%s is stale: regenerate with `go generate ./pkg/briaot` and commit the result", rel)
		}
		checked++
		return nil
	})
	if err != nil {
		t.Fatal(err)
	}
	if checked == 0 {
		t.Fatal("genbri produced no files")
	}
}
