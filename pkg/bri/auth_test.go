// --- JWT: sign / verify / alg-pin -------------------------------------------

func TestJWTSignVerifyRoundTrip(t *testing.T) {
	d := newDriver(t)
	eval(t, d, `(require '[bri.auth :as auth])`)
	// Fixed secret + clock → deterministic.
	tok := evalString(t, d, `(def t (auth/sign {:sub "u1" :role "admin"} {:secret "k" :now 1000 :exp-seconds 100})) t`)
	if strings.Count(tok, ".") != 2 {
		t.Fatalf("token is not a 3-part JWS: %q", tok)
	}
	if v := eval(t, d, `(:sub (auth/verify t {:secret "k" :now 1050}))`); v != "u1" {
		t.Fatalf("verify sub = %v, want u1", v)
	}
	if v := eval(t, d, `(:role (auth/verify t {:secret "k" :now 1050}))`); v != "admin" {
		t.Fatalf("verify role = %v", v)
	}
	// Expired.
	if v := eval(t, d, `(nil? (auth/verify t {:secret "k" :now 2000}))`); v != true {
		t.Fatalf("expired token verified")
	}
	// Wrong secret.
	if v := eval(t, d, `(nil? (auth/verify t {:secret "WRONG" :now 1050}))`); v != true {
		t.Fatalf("token verified under wrong secret")
	}
}

// The token is a REAL HS256 JWS — an independent stdlib check (a second
// implementation) agrees with bri's Go shim on header, payload, and MAC.
func TestJWTIsRealHS256(t *testing.T) {
	d := newDriver(t)
	eval(t, d, `(require '[bri.auth :as auth])`)
	tok := evalString(t, d, `(auth/sign {:sub "x"} {:secret "topsecret" :now 0 :exp-seconds 60})`)
	parts := strings.Split(tok, ".")
	if len(parts) != 3 {
		t.Fatalf("not 3 parts: %q", tok)
	}
	hdr, _ := base64.RawURLEncoding.DecodeString(parts[0])
	if string(hdr) != `{"alg":"HS256","typ":"JWT"}` {
		t.Fatalf("header = %s", hdr)
	}
	mac := hmac.New(sha256.New, []byte("topsecret"))
	mac.Write([]byte(parts[0] + "." + parts[1]))
	want := base64.RawURLEncoding.EncodeToString(mac.Sum(nil))
	if parts[2] != want {
		t.Fatalf("signature mismatch: independent stdlib HMAC disagrees")
	}
	var claims map[string]any
	pl, _ := base64.RawURLEncoding.DecodeString(parts[1])
	if err := json.Unmarshal(pl, &claims); err != nil {
		t.Fatal(err)
	}
	if claims["sub"] != "x" || claims["exp"] == nil || claims["iat"] == nil {
		t.Fatalf("claims wrong: %v", claims)
	}
}

