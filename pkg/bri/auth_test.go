// --- auto-ban: deterministic clock, threshold, cooldown, audit --------------

func TestAutoBan(t *testing.T) {
	d := newDriver(t)
	t.Setenv("APP_AUTH__SECRET", "k")
	eval(t, d, `
(require '[bri.http :as http] '[bri.auth :as auth] '[bri.audit :as audit])
(def clk (atom 0))
(def store (atom {}))
(def evs (atom []))
(audit/set-sink! (fn [e] (swap! evs conj e)))
(def ab (auth/auto-ban {:threshold 2 :window-ms 1000 :cooldown-ms 5000
                        :store store :clock (fn [] @clk)}))
;; a route that always denies (401), wrapped by auto-ban globally
(defn denyh [_] {:status 401 :body "no"})
(def rts (http/routes (http/GET "/x" #'denyh)))
(defn hit [] (:status (http/request rts {:method "GET" :path "/x" :remote-addr "9.9.9.9:1"} {:middleware [ab] :ops false})))`)
	// Under threshold: the underlying 401 passes through.
	if v := eval(t, d, `(hit)`); v != int64(401) {
		t.Fatalf("1st denial = %v, want 401", v)
	}
	// Second denial hits the threshold → client banned; NEXT request is 429.
	eval(t, d, `(hit)`) // this is the 2nd denial → triggers ban
	if v := eval(t, d, `(:status (http/request rts {:method "GET" :path "/x" :remote-addr "9.9.9.9:1"} {:middleware [ab] :ops false}))`); v != int64(429) {
		t.Fatalf("after threshold = %v, want 429 (banned)", v)
	}
	// Retry-After present.
	if v := evalString(t, d, `(get (:headers (http/request rts {:method "GET" :path "/x" :remote-addr "9.9.9.9:1"} {:middleware [ab] :ops false})) "retry-after")`); v == "" {
		t.Fatal("429 lacks Retry-After")
	}
	// Cooldown expiry → allowed again (the 401 passes through once more).
	eval(t, d, `(reset! clk 10000)`) // past the 5000ms cooldown
	if v := eval(t, d, `(hit)`); v != int64(401) {
		t.Fatalf("after cooldown = %v, want 401 (unbanned)", v)
	}
	// A ban was audited.
	if v := eval(t, d, `(some #(= :auth/auto-banned (:action %)) @evs)`); v != true {
		t.Fatal("no :auth/auto-banned audit event recorded")
	}
}

// --- perf budget (ADR 0024): a reflection-heavy JWT regression can't sneak in.
// s44 proved the Go path at ~1.1µs (sign+verify); THROUGH the interpreter the
// tree-walk overhead dominates, so the ceiling is generous and host-tunable
// (CLJGO_AUTH_PERF_MAX in µs). The gate catches a 10×+ blowup (e.g. someone
// swaps the hand-rolled HS256 for a reflection-driven lib), not micro-jitter.
func TestAuthPerfBudget(t *testing.T) {
	if testing.Short() {
		t.Skip("perf seam skipped under -short")
	}
	maxUS := 1500.0 // µs per sign+verify pair through the interpreter
	if s := os.Getenv("CLJGO_AUTH_PERF_MAX"); s != "" {
		if _, err := fmt.Sscanf(s, "%f", &maxUS); err != nil {
			t.Fatalf("CLJGO_AUTH_PERF_MAX=%q is not a number", s)
		}
	}
	d := newDriver(t)
	eval(t, d, `
(require '[bri.auth :as auth])
(defn roundtrip [] (auth/verify (auth/sign {:sub "u" :role "admin"} {:secret "k" :now 1000}) {:secret "k" :now 1000}))
(dotimes [_ 200] (roundtrip))`) // warm up
	const n = 2000
	start := time.Now()
	eval(t, d, fmt.Sprintf(`(dotimes [_ %d] (roundtrip))`, n))
	perUS := float64(time.Since(start).Microseconds()) / float64(n)
	t.Logf("sign+verify = %.1f µs/pair through the interpreter (ceiling %.0f µs; Go-native path is ~1.1 µs — s44)", perUS, maxUS)
	if perUS > maxUS {
		t.Fatalf("sign+verify %.1f µs exceeds the %.0f µs ceiling (CLJGO_AUTH_PERF_MAX)", perUS, maxUS)
	}
}

// Metrics stays off the hot path: the metrics middleware (atomic-add
// observe) must add only a small constant per request over the bare stack.
// The registry is proven O(atomic-adds) by s44's guard-overhead class; this
// gate catches a lock-contention / per-observe-alloc regression that would
// show up as the metrics stack costing multiples of the bare one.
func TestMetricsStackOverhead(t *testing.T) {
	if testing.Short() {
		t.Skip("perf seam skipped under -short")
	}
	d := newDriver(t)
	eval(t, d, `
(require '[bri.http :as http])
(defn h [_] {:status 200 :body "ok"})
(def rts (http/routes (http/GET "/x" #'h)))
(defn bare  [] (http/request rts {:method "GET" :path "/x"} {:middleware [] :ops false}))
(defn timed [] (http/request rts {:method "GET" :path "/x"} {:middleware [http/metrics] :ops false}))
(dotimes [_ 300] (bare) (timed))`) // warm
	const n = 3000
	bench := func(fn string) time.Duration {
		start := time.Now()
		eval(t, d, fmt.Sprintf(`(dotimes [_ %d] (%s))`, n, fn))
		return time.Since(start)
	}
	// The wall-clock ratio of two tiny measurements jitters badly on shared
	// runners: a single scheduler hiccup in the `bare` (denominator) sample
	// alone can spike the ratio past the ceiling on a green build (it tripped
	// macOS CI on a docs-only PR). A one-off hiccup inflates the ratio but can
	// never deflate a REAL regression below it, so take the MIN ratio over a
	// few samples — regression-faithful, immune to isolated jitter.
	const samples = 5
	ratio := math.Inf(1)
	var lastBare, lastTimed time.Duration
	for i := 0; i < samples; i++ {
		bare := bench("bare")
		timed := bench("timed")
		if r := float64(timed) / float64(bare); r < ratio {
			ratio, lastBare, lastTimed = r, bare, timed
		}
	}
	t.Logf("best of %d: bare %v/req vs +metrics %v/req = %.2fx", samples, lastBare/n, lastTimed/n, ratio)
	// A real regression is 5x+ (the middleware is a thin wrapper); the 3x
	// ceiling flags that with margin now that isolated jitter is sampled out.
	max := 3.0
	if s := os.Getenv("CLJGO_METRICS_PERF_MAX"); s != "" {
		fmt.Sscanf(s, "%f", &max)
	}
	if ratio > max {
		t.Fatalf("metrics middleware overhead %.2fx exceeds the %.1fx ceiling", ratio, max)
	}
}
