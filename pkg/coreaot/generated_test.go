package coreaot_test

import (
	"bytes"
	"os"
	"os/exec"
	"path/filepath"
	"testing"
)

// TestGeneratedCoreIsUpToDate re-runs cmd/gencore into a temp dir and
// compares byte-for-byte with the committed packages. The generated core
// is checked in (a `go build ./...` of a fresh clone must work, and the
// emitted binaries import it), so the ONE way it can lie is by drifting
// from the sources it was generated from: edit core.clj, forget
// `go generate ./pkg/coreaot`, and the interpreter and the compiled
// binary would silently disagree — the ADR 0002/0007 release blocker.
//
// Emission is deterministic by construction (design/04 §6: hoisted
// interns are sorted, forms emit in source order), so a diff here means
// exactly one thing: regenerate.
func TestGeneratedCoreIsUpToDate(t *testing.T) {
	if testing.Short() {
		t.Skip("skipping core regeneration in -short mode")
	}
	tmp := t.TempDir()
	// Run the generator as its own process from the repo root: it needs
	// the module context, and this is the same command the go:generate
	// line runs.
	cmd := exec.Command("go", "run", "./cmd/gencore", "-o", tmp)
	cmd.Dir = filepath.Join("..", "..")
	if out, err := cmd.CombinedOutput(); err != nil {
		t.Fatalf("gencore: %v\n%s", err, out)
	}

	var checked int
	err := filepath.Walk(tmp, func(path string, fi os.FileInfo, err error) error {
		if err != nil || fi.IsDir() {
			return err
		}
		rel, err := filepath.Rel(tmp, path)
		if err != nil {
			return err
		}
		fresh, err := os.ReadFile(path)
		if err != nil {
			return err
		}
		committed, err := os.ReadFile(rel)
		if err != nil {
			t.Errorf("%s: generated by cmd/gencore but not committed (run: go generate ./pkg/coreaot)", rel)
			return nil
		}
		if !bytes.Equal(fresh, committed) {
			t.Errorf("%s is stale: regenerate with `go generate ./pkg/coreaot` and commit the result", rel)
		}
		checked++
		return nil
	})
	if err != nil {
		t.Fatal(err)
	}
	if checked == 0 {
		t.Fatal("gencore produced no files")
	}
}
