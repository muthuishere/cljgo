// Command gencore AOT-compiles cljgo's own core — core.clj and every
// embedded .cljg boot source — into the Go packages under pkg/coreaot
// (ADR 0046, AOT-core piece 3). It is the generator behind
// `go generate ./pkg/coreaot`; its output is checked in, and
// pkg/coreaot/generated_test.go re-runs it into a temp dir and fails on
// any drift, so the committed packages can never disagree with the
// sources they were generated from.
//
// The compile is the interpreter's own boot, captured: one bare
// evaluator (eval.NewBare — builtins + defmacro, nothing of core.clj
// yet), then core.BootSources() in order, each read+analyzed+evaluated
// under the frame eval.loadBootSource pushes (*ns* = the source's
// namespace, *file* = its name) while pkg/emit captures the analyzed
// nodes. Compile time = eval time (ADR 0002): each form's defmacro/def
// is live for the next form's analysis, and later sources analyze
// against everything the earlier ones defined — exactly as at boot.
//
// Usage: go run ./cmd/gencore [-o pkg/coreaot]
package main

import (
	"flag"
	"fmt"
	"go/format"
	"log"
	"os"
	"path/filepath"
	"strings"

	"github.com/muthuishere/cljgo/core"
	"github.com/muthuishere/cljgo/pkg/ast"
	"github.com/muthuishere/cljgo/pkg/emit"
	"github.com/muthuishere/cljgo/pkg/eval"
	"github.com/muthuishere/cljgo/pkg/lang"
)

func main() {
	out := flag.String("o", "pkg/coreaot", "output directory for the generated packages")
	flag.Parse()
	if err := Generate(*out); err != nil {
		log.Fatalf("gencore: %v", err)
	}
}

// Generate compiles every boot source into dir/<pkg>/<pkg>.go and writes
// dir/load.go — the ordered Load() that drives them. Exported so the
// drift test can call it without shelling out.
func Generate(dir string) error {
	ev := eval.NewBare()
	// A boot source that reaches for a file-backed require is a bug in
	// the source, not a silent gap in the binary (the ADR 0042 §5 rule
	// for single-file compilation, applied to core).
	ev.LibLoader = func(_ *eval.Evaluator, lib *lang.Symbol, path string) {
		panic(fmt.Errorf("boot source required namespace %s from file %s — core's sources must be self-contained (core.BootSources)", lib.FullName(), path))
	}

	for _, s := range core.BootSources() {
		forms, err := compileSource(ev, s)
		if err != nil {
			return fmt.Errorf("compiling %s: %w", s.File, err)
		}
		formatted, raw, err := emit.EmitBootPackage(forms, s.Pkg, s.NS, s.File, emit.Options{})
		if err != nil {
			if len(raw) > 0 {
				return fmt.Errorf("emitting %s: %w\n--- unformatted ---\n%s", s.File, err, raw)
			}
			return fmt.Errorf("emitting %s: %w", s.File, err)
		}
		pkgDir := filepath.Join(dir, s.Pkg)
		if err := os.MkdirAll(pkgDir, 0o755); err != nil {
			return err
		}
		if err := os.WriteFile(filepath.Join(pkgDir, s.Pkg+".go"), formatted, 0o644); err != nil {
			return err
		}
	}
	return writeLoader(dir)
}

// compileSource reads+analyzes+evaluates one boot source through ev,
// capturing its analyzed top-level forms, under the same *ns*/*file*
// frame the interpreter's loadBootSource pushes.
func compileSource(ev *eval.Evaluator, s core.BootSource) ([]*ast.Node, error) {
	ns := lang.FindOrCreateNamespace(lang.NewSymbol(s.NS))
	lang.PushThreadBindings(lang.NewMap(lang.VarCurrentNS, ns, lang.VarFile, s.File))
	defer lang.PopThreadBindings()
	return emit.CompileSource(ev, strings.NewReader(*s.Source), s.File)
}

// writeLoader emits pkg/coreaot/load.go: the ordered Load() plus the
// init() that hands it to rt (rt cannot import this package — the
// generated packages import rt for the arithmetic intrinsics — so the
// edge is inverted through a registration, exactly as ADR 0042 does it
// for dependency namespaces).
func writeLoader(dir string) error {
	var b strings.Builder
	b.WriteString(`// Code generated by cmd/gencore. DO NOT EDIT.

// Package coreaot is cljgo's core, AOT-compiled: one Go package per
// embedded boot source (core.clj, numeric.cljg, … — core.BootSources()),
// each a Load() that runs that source's top-level forms. Linking this
// package into a binary is what makes clojure.core exist WITHOUT the
// tree-walk interpreter (ADR 0046, AOT-core piece 3).
//
// Regenerate with: go generate ./pkg/coreaot
package coreaot

//go:generate go run ../../cmd/gencore -o .

import (
	corelib "github.com/muthuishere/cljgo/pkg/corelib"
	rt "github.com/muthuishere/cljgo/pkg/emit/rt"
`)
	for _, s := range core.BootSources() {
		fmt.Fprintf(&b, "\t%s %q\n", s.Pkg, "github.com/muthuishere/cljgo/pkg/coreaot/"+s.Pkg)
	}
	b.WriteString(`)

// init hands Load to rt so rt.Boot() can run it without importing this
// package (the generated packages import rt). A binary links coreaot by
// blank-importing it; rt.Boot() then finds the loader registered.
func init() { rt.RegisterCoreLoader(Load) }

var loaded = false

// Load builds the core namespace world in core.BootSources() order —
// the same sources, in the same order, as eval.New()'s interpreted boot
// (design/00 §6) — then interns the user namespace (corelib.InitUserNS,
// shared with the interpreter). Idempotent.
func Load() {
	if loaded {
		return
	}
	loaded = true
`)
	for _, s := range core.BootSources() {
		fmt.Fprintf(&b, "\t%s.Load()\n", s.Pkg)
	}
	b.WriteString(`	corelib.InitUserNS()
}
`)
	formatted, err := format.Source([]byte(b.String()))
	if err != nil {
		return fmt.Errorf("formatting load.go: %w\n%s", err, b.String())
	}
	return os.WriteFile(filepath.Join(dir, "load.go"), formatted, 0o644)
}
